Polygonizer::Polygonizer(ScalarField* field) {
	scalarField = field;

	int i, j, k;
	scalarField->getGridDimensions(&i, &j, &k);
	isize = i - 1;
	jsize = j - 1;
	ksize = k - 1;
	cellsize = scalarField->getCellSize();
	surfaceThreshold = scalarField->getSurfaceThreshold();

	isScalarFieldSet = true;
}

void Polygonizer::setSurfaceCellMask(Array3D<bool>* mask) {
	surfaceCellMask = mask;
	isSurfaceCellMaskSet = true;
}

bool Polygonizer::isCellOnSurface(Point3i idx) {
	Point3i vertices[8];
	getGridIndexVertices(idx.x, idx.y, idx.z, vertices);

	bool hasInside = false;
	bool hasOutside = false;
	for (int i = 0; i < 8; i++) {
		Point3i v = vertices[i];
		if (scalarField->getScalarFieldValue(v.x, v.y, v.z) > surfaceThreshold) {
			hasInside = true;
		} else {
			hasOutside = true;
		}
	}

	return hasInside && hasOutside;
}

void Polygonizer::findSurfaceCells(std::vector<Point3i>& surfaceCells) {
	bool isEmpty = true;
	for (int k = 0; k < ksize + 1; k++) {
		for (int j = 0; j < jsize + 1; j++) {
			for (int i = 0; i < isize + 1; i++) {
				if (scalarField->getScalarFieldValue(i, j, k) > surfaceThreshold) {
					isEmpty = false;
					goto endLoop;
				}
			}
		}
	}
endLoop:

	if (isEmpty) {
		return;
	}

	for (int k = 0; k < ksize; k++) {
		for (int j = 0; j < jsize; j++) {
			for (int i = 0; i < isize; i++) {
				Point3i cell(i, j, k);

				if (isSurfaceCellMaskSet && !surfaceCellMask->get(cell.x, cell.y, cell.z)) {
					continue;
				}

				if (isCellOnSurface(cell)) {
					surfaceCells.push_back(cell);
				}
			}
		}
	}
}

int Polygonizer::calculateCubeIndex(Point3i cell) {
	Point3i vs[8];
	getGridIndexVertices(cell.x, cell.y, cell.z, vs);

	int cubeIndex = 0;
	if (scalarField->getScalarFieldValue(vs[0].x, vs[0].y, vs[0].z) > surfaceThreshold) {
		cubeIndex |= 1;
	}
	if (scalarField->getScalarFieldValue(vs[1].x, vs[1].y, vs[1].z) > surfaceThreshold) {
		cubeIndex |= 2;
	}
	if (scalarField->getScalarFieldValue(vs[2].x, vs[2].y, vs[2].z) > surfaceThreshold) {
		cubeIndex |= 4;
	}
	if (scalarField->getScalarFieldValue(vs[3].x, vs[3].y, vs[3].z) > surfaceThreshold) {
		cubeIndex |= 8;
	}
	if (scalarField->getScalarFieldValue(vs[4].x, vs[4].y, vs[4].z) > surfaceThreshold) {
		cubeIndex |= 16;
	}
	if (scalarField->getScalarFieldValue(vs[5].x, vs[5].y, vs[5].z) > surfaceThreshold) {
		cubeIndex |= 32;
	}
	if (scalarField->getScalarFieldValue(vs[6].x, vs[6].y, vs[6].z) > surfaceThreshold) {
		cubeIndex |= 64;
	}
	if (scalarField->getScalarFieldValue(vs[7].x, vs[7].y, vs[7].z) > surfaceThreshold) {
		cubeIndex |= 128;
	}

	return cubeIndex;
}

Point3f Polygonizer::vertexInterpolate(Point3f p1, Point3f p2, double v1, double v2) {
	if (fabs(surfaceThreshold - v1) < ShadowEpsilon || fabs(v1 - v2) < ShadowEpsilon) {
		Vector3f v = normalize(p2 - p1);
		return p1 + (float)(ShadowEpsilon * cellsize) * v;
	}
	if (fabs(surfaceThreshold - v2) < ShadowEpsilon) {
		Vector3f v = normalize(p2 - p1);
		return p2 - (float)(ShadowEpsilon * cellsize) * v;
	}

	double mu = (surfaceThreshold - v1) / (v2 - v1);
	return p1 + (float)mu * (p2 - p1);
}

void Polygonizer::calculateVertexList(Point3i cell, int cubeIndex, Polygonizer::EdgeGrid& edges, std::vector<Point3f>& meshVertices, int* vertexList) {
	Point3i vertices[8];
	double values[8];
	Point3f positions[8];
	Point3f np;

	getGridIndexVertices(cell.x, cell.y, cell.z, vertices);
	for (int i = 0; i < 8; i++) {
		values[i] = scalarField->getScalarFieldValue(vertices[i].x, vertices[i].y, vertices[i].z);
		positions[i] = Point3f(vertices[i].x, vertices[i].y, vertices[i].z) * cellsize;
	}

	if (edgeTable[cubeIndex] & 1) {
		if (edges.U(vertices[0].x, vertices[0].y, vertices[0].z) == -1) {
			np = vertexInterpolate(positions[0], positions[1], values[0], values[1]);
			meshVertices.push_back(np);
			edges.U.set(vertices[0].x, vertices[0].y, vertices[0].z, (int)meshVertices.size() - 1);
		}
		vertexList[0] = edges.U(vertices[0].x, vertices[0].y, vertices[0].z);
	}
	if (edgeTable[cubeIndex] & 2) {
		if (edges.W(vertices[1].x, vertices[1].y, vertices[1].z) == -1) {
			np = vertexInterpolate(positions[1], positions[2], values[1], values[2]);
			meshVertices.push_back(np);
			edges.W.set(vertices[1].x, vertices[1].y, vertices[1].z, (int)meshVertices.size() - 1);
		}
		vertexList[1] = edges.W(vertices[1].x, vertices[1].y, vertices[1].z);
	}
	if (edgeTable[cubeIndex] & 4) {
		if (edges.U(vertices[3].x, vertices[3].y, vertices[3].z) == -1) {
			np = vertexInterpolate(positions[2], positions[3], values[2], values[3]);
			meshVertices.push_back(np);
			edges.U.set(vertices[3].x, vertices[3].y, vertices[3].z, (int)meshVertices.size() - 1);
		}
		vertexList[2] = edges.U(vertices[3].x, vertices[3].y, vertices[3].z);
	}
	if (edgeTable[cubeIndex] & 8) {
		if (edges.W(vertices[0].x, vertices[0].y, vertices[0].z) == -1) {
			np = vertexInterpolate(positions[3], positions[0], values[3], values[0]);
			meshVertices.push_back(np);
			edges.W.set(vertices[0].x, vertices[0].y, vertices[0].z, (int)meshVertices.size() - 1);
		}
		vertexList[3] = edges.W(vertices[0].x, vertices[0].y, vertices[0].z);
	}
	if (edgeTable[cubeIndex] & 16) {
		if (edges.U(vertices[4].x, vertices[4].y, vertices[4].z) == -1) {
			np = vertexInterpolate(positions[4], positions[5], values[4], values[5]);
			meshVertices.push_back(np);
			edges.U.set(vertices[4].x, vertices[4].y, vertices[4].z, (int)meshVertices.size() - 1);
		}
		vertexList[4] = edges.U(vertices[4].x, vertices[4].y, vertices[4].z);
	}
	if (edgeTable[cubeIndex] & 32) {
		if (edges.W(vertices[5].x, vertices[5].y, vertices[5].z) == -1) {
			np = vertexInterpolate(positions[5], positions[6], values[5], values[6]);
			meshVertices.push_back(np);
			edges.W.set(vertices[5].x, vertices[5].y, vertices[5].z, (int)meshVertices.size() - 1);
		}
		vertexList[5] = edges.W(vertices[5].x, vertices[5].y, vertices[5].z);
	}
	if (edgeTable[cubeIndex] & 64) {
		if (edges.U(vertices[7].x, vertices[7].y, vertices[7].z) == -1) {
			np = vertexInterpolate(positions[6], positions[7], values[6], values[7]);
			meshVertices.push_back(np);
			edges.U.set(vertices[7].x, vertices[7].y, vertices[7].z, (int)meshVertices.size() - 1);
		}
		vertexList[6] = edges.U(vertices[7].x, vertices[7].y, vertices[7].z);
	}
	if (edgeTable[cubeIndex] & 128) {
		if (edges.W(vertices[4].x, vertices[4].y, vertices[4].z) == -1) {
			np = vertexInterpolate(positions[7], positions[4], values[7], values[4]);
			meshVertices.push_back(np);
			edges.W.set(vertices[4].x, vertices[4].y, vertices[4].z, (int)meshVertices.size() - 1);
		}
		vertexList[7] = edges.W(vertices[4].x, vertices[4].y, vertices[4].z);
	}
	if (edgeTable[cubeIndex] & 256) {
		if (edges.V(vertices[0].x, vertices[0].y, vertices[0].z) == -1) {
			np = vertexInterpolate(positions[0], positions[4], values[0], values[4]);
			meshVertices.push_back(np);
			edges.V.set(vertices[0].x, vertices[0].y, vertices[0].z, (int)meshVertices.size() - 1);
		}
		vertexList[8] = edges.V(vertices[0].x, vertices[0].y, vertices[0].z);
	}
	if (edgeTable[cubeIndex] & 512) {
		if (edges.V(vertices[1].x, vertices[1].y, vertices[1].z) == -1) {
			np = vertexInterpolate(positions[1], positions[5], values[1], values[5]);
			meshVertices.push_back(np);
			edges.V.set(vertices[1].x, vertices[1].y, vertices[1].z, (int)meshVertices.size() - 1);
		}
		vertexList[9] = edges.V(vertices[1].x, vertices[1].y, vertices[1].z);
	}
	if (edgeTable[cubeIndex] & 1024) {
		if (edges.V(vertices[2].x, vertices[2].y, vertices[2].z) == -1) {
			np = vertexInterpolate(positions[2], positions[6], values[2], values[6]);
			meshVertices.push_back(np);
			edges.V.set(vertices[2].x, vertices[2].y, vertices[2].z, (int)meshVertices.size() - 1);
		}
		vertexList[10] = edges.V(vertices[2].x, vertices[2].y, vertices[2].z);
	}
	if (edgeTable[cubeIndex] & 2048) {
		if (edges.V(vertices[3].x, vertices[3].y, vertices[3].z) == -1) {
			np = vertexInterpolate(positions[3], positions[7], values[3], values[7]);
			meshVertices.push_back(np);
			edges.V.set(vertices[3].x, vertices[3].y, vertices[3].z, (int)meshVertices.size() - 1);
		}
		vertexList[11] = edges.V(vertices[3].x, vertices[3].y, vertices[3].z);
	}
}

void Polygonizer::polygonizeCell(Point3i cell, Polygonizer::EdgeGrid& edges, TriangleMesh& mesh) {
	int cubeIdx = calculateCubeIndex(cell);

	if (edgeTable[cubeIdx] == 0) {
		// cube is entirely inside/outside of surface
		return;
	}

	int vertexList[12];
	calculateVertexList(cell, cubeIdx, edges, mesh.vertices, vertexList);

	for (int i = 0; triTable[cubeIdx][i] != -1; i += 3) {
		Point3i triIdx = Point3i(vertexList[triTable[cubeIdx][i]], vertexList[triTable[cubeIdx][i + 1]], vertexList[triTable[cubeIdx][i + 2]]);
		mesh.indices.push_back(triIdx);
	}
}

void Polygonizer::calculateSurfaceTriangles(std::vector<Point3i>& surfaceCells, TriangleMesh& mesh) {
	EdgeGrid edges(isize, jsize, ksize);
	for (int i = 0; i < surfaceCells.size(); i++) {
		polygonizeCell(surfaceCells[i], edges, mesh);
	}
}

TriangleMesh Polygonizer::polygonizeSurface() {
	std::vector<Point3i> surfaceCells(isize * jsize * ksize);
	findSurfaceCells(surfaceCells);

	TriangleMesh mesh;
	calculateSurfaceTriangles(surfaceCells, mesh);

	mesh.updateVertexNormals();

	return mesh;
}

